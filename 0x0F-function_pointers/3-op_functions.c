/**
 * op_add - adds
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract or difference
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - products of 2 integers
 * @a: integer
 * @b: integer
 * Return: result
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of 2 integers
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - reminder of the division of a and b
 * @a: integer
 * @b: integer
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
